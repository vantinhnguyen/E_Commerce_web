     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {   
        if ($request->check == 'status') {
            $user = User::find($id)->update(['status'=>$request->status]);
            if ($user) {
                return redirect()->route('account.index')->with('ss','Cập nhật trạng thái tài khoản thành công!');
            }
        }else if ($request->check == 'role') {
            $user = User::find($id)->update(['role'=>$request->role]);
            if ($user) {
                return redirect()->route('account.index')->with('ss','Cập nhật quyền thành công!');
            }
        }
    }
        

